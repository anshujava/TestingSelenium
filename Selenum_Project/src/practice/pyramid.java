package practice;

public class pyramid {

	public static void main(String[] args) {
		for (int i = 1; i <= 5; i++) 
		{ 
			for (int k = 1; k <= i; k++) {
				System.out.print(k + " "); 
				} 
			System.out.println(); 
			
			} 
		for (int a = 4; a >0; a--) {
            for (int b = 1; b <= a; b++) {
                  System.out.print(b+" ");
            }
            System.out.println("");
     }
		
	}
	}

